  	Το Τμήμα Πληροφορικής και Τηλεπικοινωνιών του Πανεπιστημίου Ιωαννίνων έχει έδρα την πόλη της Άρτας. Ιδρύθηκε με βάση το άρθρο 2 του νόμου 4559 (ΦΕΚ 142, τεύχος Α΄/03.08.2018). Με το άρθρο 3 του ιδίου νόμου, ιδρύθηκε το ομώνυμο τμήμα.

Αποστολή του Τμήματος είναι η παροχή εκπαίδευσης, τεχνογνωσίας και εξειδίκευσης υψηλού επιπέδου, καθώς και η ανάπτυξη έρευνας σε αναδυόμενους τομείς της Πληροφορικής και των Τηλεπικοινωνιών, με σκοπό τη δημιουργία στελεχών υψηλού επιστημονικού επιπέδου. Η δισδιάστατη προσέγγιση των αντικειμένων της Πληροφορικής και των Τηλεπικοινωνιών, επιτρέπει την δημιουργία επαγγελματιών με υψηλή εξειδίκευση και ελκυστικότητα στην αγορά εργασίας.

Το Τμήμα Πληροφορικής & Τηλεπικοινωνιών αποτελεί ένα ενεργό κύτταρο παραγωγής γνώσης και ευκαιριών για τους νέους που επιδιώκουν μια επιτυχή καριέρα στον χώρο της Πληροφορικής και των Τηλεπικοινωνιών. Το Τμήμα δεσμεύεται για την παροχή υψηλής ποιότητας σπουδών στους φοιτητές του, σύμφωνα με την πολιτική ποιότητας του ιδρύματος.

Το τμήμα έχει πλέον της 20ετής ακαδημαϊκής διαδρομής (ίδρυση 26-5-1998 ως Τμήμα του ΤΕΙ Ηπείρου), με πάνω από 600 ενεργούς φοιτητές, 16 μέλη ΔΕΠ, 2 μέλη ΕΔΙΠ και 7 καθηγητές απόκτησης διδακτικής Εμπειρίας και με 407.
